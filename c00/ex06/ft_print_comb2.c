#include <unistd.h>

void	ft_print_comb2(void)
{
	char	a;
	char	b;
	char	k;

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			k = a / 10 + '0';
			write(1, &k, 1);
			k = a % 10 + '0';
			write(1, &k, 1);
			write(1, " ", 1);
			k = b / 10 + '0';
			write(1, &k, 1);
			k = b % 10 + '0';
			write(1, &k, 1);
			if (a != 98)
				write (1, ", ", 2);
			++b;
		}
		++a;
	}
}

